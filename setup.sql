SET NAMES utf8mb4;
SET FOREIGN_KEY_CHECKS = 0;

DROP TABLE IF EXISTS `users`;
DROP TABLE IF EXISTS `ip_analytics`;
DROP TABLE IF EXISTS `ip_assets`;
DROP TABLE IF EXISTS `contracts`;

-- 表 1: 用户表
CREATE TABLE `users` (
  `user_id` INT NOT NULL AUTO_INCREMENT,
  `username` VARCHAR(100) NOT NULL UNIQUE COMMENT '用户名',
  `password_hash` VARCHAR(255) NOT NULL COMMENT '加盐后的密码哈希值',
  `role` ENUM('internal', 'partner') NOT NULL COMMENT '用户角色',
  `created_at` TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 表 2: IP资产台账
CREATE TABLE `ip_assets` (
  `ip_id` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(255) NOT NULL COMMENT 'IP名称',
  `tags` VARCHAR(255) DEFAULT NULL COMMENT '标签',
  `description` TEXT DEFAULT NULL COMMENT '创作说明',
  `image_url` VARCHAR(255) DEFAULT NULL COMMENT '图样',
  `author` VARCHAR(100) DEFAULT NULL COMMENT '作者',
  `ownership` VARCHAR(255) DEFAULT NULL COMMENT '权属',
  `license_period` VARCHAR(100) DEFAULT NULL COMMENT '可授权期限',
  `reg_number` VARCHAR(100) UNIQUE DEFAULT NULL COMMENT '登记号',
  `reg_date` DATE DEFAULT NULL COMMENT '登记日期',
  `license_type_options` VARCHAR(255) DEFAULT NULL COMMENT '可提供的许可类型',
  `trademark_info` TEXT DEFAULT NULL COMMENT '已核准注册的相关商标',
  `contact_email` VARCHAR(100) DEFAULT NULL COMMENT '详情联系员工邮箱',
  `value_level` ENUM('S', 'A', 'B', 'C') DEFAULT NULL COMMENT '商业价值级别',
  `current_revenue` DECIMAL(12,2) DEFAULT 0.00 COMMENT '目前收益（万元）',
  `internal_status` VARCHAR(50) DEFAULT '可许可' COMMENT '内部管理状态',
  `cooperation_count` INT DEFAULT 0 COMMENT '合作次数',
  PRIMARY KEY (`ip_id`),
  INDEX `idx_name` (`name`),
  INDEX `idx_tags` (`tags`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 表 3: 合同台账
CREATE TABLE `contracts` (
  `contract_id` INT NOT NULL AUTO_INCREMENT,
  `ip_id` INT NOT NULL COMMENT '关联的IP ID',
  `partner_name` VARCHAR(255) NOT NULL COMMENT '相对方名称',
  `partner_brand` VARCHAR(255) DEFAULT NULL COMMENT '相对方品牌',
  `region` VARCHAR(255) DEFAULT NULL COMMENT '许可地域',
  `media` VARCHAR(255) DEFAULT NULL COMMENT '许可媒介',
  `license_method` VARCHAR(255) DEFAULT NULL COMMENT '许可方式',
  `license_category` VARCHAR(255) DEFAULT NULL COMMENT '许可类别',
  `usage_type` VARCHAR(255) DEFAULT NULL COMMENT '许可使用方式',
  `license_type` ENUM('独占许可', '排他许可', '普通许可') NOT NULL COMMENT '许可类型',
  `term_start` DATE NOT NULL COMMENT '许可期限开始',
  `term_end` DATE NOT NULL COMMENT '许可期限结束',
  `fee_standard` TEXT DEFAULT NULL COMMENT '许可费标准',
  `payment_cycle` TEXT DEFAULT NULL COMMENT '结算周期',
  `case_image_url` VARCHAR(255) DEFAULT NULL COMMENT '合作案例图片路径',
  `pdf_url` VARCHAR(255) DEFAULT NULL COMMENT '合同原件PDF路径',
  PRIMARY KEY (`contract_id`),
  FOREIGN KEY (`ip_id`) REFERENCES `ip_assets` (`ip_id`) ON DELETE RESTRICT
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- 表 4: 点击量
CREATE TABLE `ip_analytics` (
  `an_id` INT NOT NULL AUTO_INCREMENT,
  `ip_id` INT NOT NULL,
  `user_id` INT NULL,
  PRIMARY KEY (`an_id`),
  FOREIGN KEY (`ip_id`) REFERENCES `ip_assets` (`ip_id`),
  FOREIGN KEY (`user_id`) REFERENCES `users` (`user_id`)
);

